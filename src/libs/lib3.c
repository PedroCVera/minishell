#include "../../minishell.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*chr;

	chr = b;
	i = 0;
	while (i < len)
	{		
		chr[i] = (char) c;
		i++;
	}
	return (b);
}
